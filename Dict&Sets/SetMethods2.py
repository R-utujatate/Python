'''
set.union(set2) - Combines both set values and return new
set.intersection(set1) - combines common values and return new
'''

Group1={1,2,3,4}
Group2={3,4,5,6}

print("First set : ",Group1)
print("Second set : ",Group2)

print("Union : ",Group1.union(Group2))

print("Intersection : ",Group1.intersection(Group2))